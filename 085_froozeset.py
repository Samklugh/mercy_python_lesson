# FROOSE SET IN PYTHON 
# A frooze set is an immutable version of a set. It is created using the frozenset() function. A frooze set cannot be modified after it is created, meaning you cannot add or remove items from it. However, you can perform set operations on frooze sets, such as union, intersection, difference, and symmetric difference.

# creating a frooze set
my_frooze_set = frozenset(["apple", "banana", "cherry"])
print(my_frooze_set) # this will print frozenset({'apple', 'banana', 'cherry'})